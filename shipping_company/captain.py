class Captain:

    def __init__(self,
                 name: str,
                 surname: str,
                 experience: int,
                 qualification: str
                 ) -> None:

        self.name = name  # имя капитана
        self.surname = surname  # фамилия капитана
        self.experience = experience  # опыт капитана
        self.qualification = qualification  # квалификация капитана
